    private void AgregarMedicamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarMedicamentoActionPerformed
        txtArea.setText("");
        String nombreMedicamento = this.nombreMedicamento.getText();
        int precioMedicamento = Integer.parseInt(this.precioMedicamento.getText());
        String descripcionMedicamento = this.descripcionMedicamento.getText();
        String fabricanteMedicamento = this.fabricanteMedicamento.getText();
        String presentacionMedicamento = this.presentacionMedicamento.getText();
        int stockMedicamento = Integer.parseInt(this.stockMedicamento.getText());
        String tipoMedicamento = this.tipoMedicamento.getText();
        int codigoMedicamento = Integer.parseInt(this.codigoMedicamento.getText());
        Medicamento medicina = new Medicamento(nombreMedicamento, precioMedicamento, descripcionMedicamento,
                fabricanteMedicamento, presentacionMedicamento, stockMedicamento, tipoMedicamento,
                codigoMedicamento);
        lista.agregarMedicamento(medicina);
        txtArea.append("Nombre" + "\t" + "Precio" + "\t" + "Fabricante" + "\t" + "Stock" + "\t" + "Codigo" + "\t" + "Descripcion" + "\t" + "Presentacion" + "\t" + "Tipo" + "\n");
        txtArea.append(lista.imprimirLista());


    }//GEN-LAST:event_AgregarMedicamentoActionPerformed

