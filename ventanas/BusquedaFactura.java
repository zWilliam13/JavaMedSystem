    public BusquedaFactura() {
        initComponents();
        for (int i = 0; i < facturas.length; i++) {
            String[] nombres = {"Ana", "Carlos", "David", "Elena", "Fernando", "Gloria", "Hugo", "Isabel", "Jorge", "Laura"};
            int numero = (int) (Math.random() * 100 + 1);
             int factura = (int) (Math.random() * 100 + 1);
            int fecha = (int) (Math.random() * 30 + 1);
            int mes = (int) (Math.random() * 12 + 1);
            int posicion = (int) (Math.random() * nombres.length);
            facturas[i] = new Factura("F002-" + factura, numero, nombres[posicion], LocalDate.of(2023, mes, fecha), factura);
        }

        for (int i = 0; i < facturas.length; i++) {
            arbol.insertar(facturas[i]);
        }

        for (int i = 0; i < facturas.length; i++) {
            texto = texto + facturas[i].getNumeroFactura() + "\t"
                    + facturas[i].getNombreCliente()
                    + "\t" + facturas[i].getPrecioFinal() + "\t" + facturas[i].getFecha() + "\n";
        }

        System.out.println(texto);

    }

