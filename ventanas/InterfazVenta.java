    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nombreCliente = this.nombreCliente.getText();
            String nombreMedicamento = this.nombreMedicamento.getText();
            String cantMedica = this.cantidadMedicamento.getText();
            String precioMedi = this.cantidadPrecio.getText();
            if (nombreCliente.isEmpty() || nombreMedicamento.isEmpty() || cantMedica.isEmpty() || precioMedi.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Faltan datos",
                        "Alerta", JOptionPane.WARNING_MESSAGE);
            } else {
                int cantidadMedicamento = Integer.parseInt(cantMedica);
                double precio = Double.parseDouble(precioMedi);
                if (cantidadMedicamento >= 0 && precio >= 0) {
                    Venta cliente = new Venta(nombreMedicamento, cantidadMedicamento, precio, nombreCliente);

                    if (!pila.pilaLlena()) {
                        this.nombreCliente.setText("");
                        this.nombreMedicamento.setText("");
                        this.cantidadMedicamento.setText("");
                        this.cantidadPrecio.setText("");
                        txtArea.setText("");
                        pila.insertar(cliente);
                        txtArea.append("Nombre de Cliente " + "\t" + " Medicamento" + "\t" + "\t"
                                + "Precio" + "\t" + "Cantidad" + "\t"
                                + "Total" + "\t" + "Fecha" + "\t" + "Hora" + "\n");
                        txtArea.append(pila.imprimirPila());
                    } else {
                        JOptionPane.showMessageDialog(null, "Pila llena");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Datos incorrectos",
                            "Alerta", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

